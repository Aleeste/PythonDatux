## iteracion de una lista de numeros  que identifique si es multiplico de  2 e imprimir el numero
Listanumero=[1,2]
for N in Listanumero:
        if(N%2==0):
                print("Si es multiplico")

        else : 
                print("No es multiplico")
        
           
        







 





        
       
 





       


















